package ch02.sec06;

public class stringExample {
	public static void main(String[] args) {
		String name = "홍길동";
		String job = "프로그래머";
		System.out.println(name);
		System.out.println(job);
		
		String str = "나는\"자바\"를 '배움' ";
		System.out.println(str);
		
		str = "번호\t이름\t작업 ";
		System.out.println(str);
		
		System.out.print("나는\n자바\n배움");
	}
}
